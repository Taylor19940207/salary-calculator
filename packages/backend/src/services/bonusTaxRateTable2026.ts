/**
 * 賞与の源泉徴収の率(%)を返す。
 * @param dependents 扶養親族等の数
 * @param prevMonthAfterInsurance 前月の社会保険料等控除後の給与等の金額（円）
 */
export function bonusTaxRate(dependents: number, prevMonthAfterInsurance: number): number {
  const col = BONUS_RATE_BANDS_2026[Math.max(0, Math.min(dependents, 7))];
  let rate = 0;
  for (const [ijouThousand, r] of col) {
    if (prevMonthAfterInsurance >= ijouThousand * 1000) rate = r;
    else break;
  }
  return rate;
}
